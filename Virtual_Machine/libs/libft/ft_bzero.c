#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	char			*cpy;

	i = 0;
	cpy = (char *)s;
	while (i < n)
		cpy[i++] = '\0';
	s = (void *)cpy;
}
